package com.onesoft.day6_for;

public class ForwardNumber {
	public static void main(String[] args) {
	//int i;
		for(int i=1;i<=10;i++) {
			System.out.println(i);
		}
		//System.out.println(i);to use i value outside we have to declare i value out of for
		for(int i=100;i<=200;i++) {
			System.out.println(i);
		}
		for(int i=1;i<=50;i++) {
			System.out.println("Maheswari");
		}
	}

}
